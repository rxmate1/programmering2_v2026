/*
Dette er en helt grunnleggende kjørbar klasse i Java
 */
public class HelloWorld {
    /*
    Dette er en main()-metode. Dette er hvor vi definerer koden som skal kjøre i programmet vårt.
    All kode som skal kjøres må kalles direkte eller inndirekte fra en slik main()-metode.
    Hver klasse som inneholder en slik main()-metode kan anses som et eget og selvstendig program og vi kan ha mange
    slike i et IntelliJ IDEA-prosjekt.
    Ikke tenk på nøkkelord, slik som static, void, String[] osv. helt enda.
     */
    public static void main(String[] args) {
        // Dette skriver ut en linje til standard output.
        System.out.println("Hello world!");
    }
}
